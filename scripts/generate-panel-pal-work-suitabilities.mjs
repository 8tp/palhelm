#!/usr/bin/env node

import { mkdir, readFile, writeFile } from "node:fs/promises";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const usage =
  "Usage: node scripts/generate-panel-pal-work-suitabilities.mjs <normalized-pal-knowledge.json> [output.ts]";

if (process.argv[2] === "--help" || process.argv[2] === "-h") {
  console.log(usage);
  process.exit(0);
}

if (!process.argv[2]) {
  console.error(usage);
  process.exit(2);
}

const sourcePath = resolve(process.cwd(), process.argv[2]);
const outputPath = process.argv[3]
  ? resolve(process.cwd(), process.argv[3])
  : join(repoRoot, "frontend/src/data/palWorkSuitabilities.generated.ts");

const parsed = JSON.parse(await readFile(sourcePath, "utf8"));
if (!parsed || typeof parsed !== "object" || !Array.isArray(parsed.pals) || !parsed.metadata) {
  throw new Error(`invalid Pal knowledge snapshot: ${sourcePath}`);
}

const rows = {};
for (const pal of [...parsed.pals].sort((a, b) => String(a.internalId).localeCompare(String(b.internalId)))) {
  if (typeof pal.internalId !== "string" || pal.internalId.trim() === "" || !Array.isArray(pal.workSuitabilities)) {
    throw new Error("Pal knowledge contains an invalid species row");
  }
  const key = pal.internalId.toLocaleLowerCase("en-US");
  if (Object.hasOwn(rows, key)) throw new Error(`duplicate Pal CharacterID: ${pal.internalId}`);
  rows[key] = pal.workSuitabilities.map((work) => {
    if (
      !work ||
      typeof work.id !== "string" ||
      typeof work.name !== "string" ||
      !Number.isInteger(work.level) ||
      work.level <= 0
    ) {
      throw new Error(`invalid work suitability for ${pal.internalId}`);
    }
    return [work.id, work.name, work.level];
  });
}

const generatedAt = typeof parsed.metadata.generatedAt === "string" ? parsed.metadata.generatedAt : "";
const sources = Array.isArray(parsed.metadata.sources)
  ? parsed.metadata.sources.map(({ name, version, url, attribution }) => ({ name, version, url, attribution }))
  : [];
const output = `// Generated by scripts/generate-panel-pal-work-suitabilities.mjs. Do not edit by hand.\n` +
  `// This is species metadata from the repository's version-pinned public Paldeck snapshot;\n` +
  `// individual Pal save observations continue to come from the panel API.\n\n` +
  `export type PalWorkSuitabilityRow = readonly [id: string, name: string, level: number];\n\n` +
  `export const PAL_WORK_DATA_GENERATED_AT = ${JSON.stringify(generatedAt)};\n` +
  `export const PAL_WORK_DATA_SOURCES = ${JSON.stringify(sources, null, 2)} as const;\n\n` +
  `export const PAL_WORK_SUITABILITY_ROWS: Readonly<Record<string, readonly PalWorkSuitabilityRow[]>> = ${JSON.stringify(rows, null, 2)};\n`;

await mkdir(dirname(outputPath), { recursive: true });
await writeFile(outputPath, output, "utf8");
console.log(`Wrote ${Object.keys(rows).length} Pal work-suitability rows to ${outputPath}`);
